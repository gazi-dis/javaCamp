
public class Student extends User{
	private String takenCourse;
	private int takenCoursesCount;
	
	public String getTakenCourse() {
		return takenCourse;
	}
	public void setTakenCourse(String takenCourse) {
		this.takenCourse = takenCourse;
	}
	public int getTakenCoursesCount() {
		return takenCoursesCount;
	}
	public void setTakenCoursesCount(int takenCoursesCount) {
		this.takenCoursesCount = takenCoursesCount;
	}
	
	
}
